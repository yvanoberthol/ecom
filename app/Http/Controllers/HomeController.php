    public function top_10_settings(Request $request)
    {
        foreach (Category::all() as $key => $category) {
            if(is_array($request->top_categories) &&
                in_array($category->id, $request->top_categories, true)){
                $category->top = 1;
                $category->save();
            }
            else{
                $category->top = 0;
                $category->save();
            }
        }

        foreach (Brand::all() as $key => $brand) {
            if(is_array($request->top_brands) &&
                in_array($brand->id, $request->top_brands, true)){
                $brand->top = 1;
                $brand->save();
